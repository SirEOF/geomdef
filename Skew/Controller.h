#ifndef CONTROLLER_H
#define CONTROLLER_H

#include <QtXml>

/////////////////////////////////////////////////////////////////////////
/// \class Controller
/// Klasa definujaca Kontroler z modelu MVC.
/////////////////////////////////////////////////////////////////////////
class Controller
{

public:
    //////////////////////////////////////////////////////////////////////////
    /// Konstruktor ktory wczytuje plik cameras.xml z parametrami obiektywów.
    /// Kolejne obiektywy sa wyswietlane w liscie (comboBox).
    //////////////////////////////////////////////////////////////////////////
    Controller();

    //////////////////////////////////////////////////////////////////////////
    /// Destruktor. Kasuje wczytanego XMLa.
    //////////////////////////////////////////////////////////////////////////
    ~Controller();

    /////////////////////////////////////////////////////////////////////////
    /// Metoda getParams zwraca parametry z XMLa
    /// \param name nazwa obiektywu
    /// \return Zwraca tablice z parametrami a b c d.
    /////////////////////////////////////////////////////////////////////////
    qreal* getParams(QString name);

    //////////////////////////////////////////////////////////////////////////
    /// Getter nazw obiektywow
    /// \return Zwraca QVector Stringow z nazwami obiektywow
    //////////////////////////////////////////////////////////////////////////
    QStringList getNames() const
    {
        return names;
    }

private:
    //////////////////////////////////////////////////////////////////////////
    /// Wskaznik do dokumentu DOM. Przechowuje XMLa z parametrami obiektywow.
    //////////////////////////////////////////////////////////////////////////
    QDomDocument *doc;

    //////////////////////////////////////////////////////////////////////////
    /// Lista Stringow z nazwami obiektywow.
    //////////////////////////////////////////////////////////////////////////
    QStringList names;

    //////////////////////////////////////////////////////////////////////////
    /// Tablica parametrow obiektywow.
    //////////////////////////////////////////////////////////////////////////
    qreal params[4];

};

#endif // CONTROLLER_H
